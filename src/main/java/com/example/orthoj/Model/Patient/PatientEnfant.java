package com.example.orthoj.Model.Patient;

public class PatientEnfant {
    String Classe ;
    String nomPere ;
    String prenomPere;
    String nomMere ;
    String prenomMere ;
    String numPapa ;
    String numMama ;

    public String getClasse() {
        return Classe;
    }

    public void setClasse(String classe) {
        Classe = classe;
    }

    public String getNomPere() {
        return nomPere;
    }

    public void setNomPere(String nomPere) {
        this.nomPere = nomPere;
    }

    public String getPrenomPere() {
        return prenomPere;
    }

    public void setPrenomPere(String prenomPere) {
        this.prenomPere = prenomPere;
    }

    public String getNomMere() {
        return nomMere;
    }

    public void setNomMere(String nomMere) {
        this.nomMere = nomMere;
    }

    public String getPrenomMere() {
        return prenomMere;
    }

    public void setPrenomMere(String prenomMere) {
        this.prenomMere = prenomMere;
    }

    public String getNumPapa() {
        return numPapa;
    }

    public void setNumPapa(String numPapa) {
        this.numPapa = numPapa;
    }

    public String getNumMama() {
        return numMama;
    }

    public void setNumMama(String numMama) {
        this.numMama = numMama;
    }
}
